package com.crowninteractive.smsportal.dto;

/**
 *
 * @author adekanmbi
 */
public class EMCC {

    private String message;
    private String phone;
    private String name;

    private String meterReading;
    private String meterNumber;
    private String district;

    private String accountNumber;

    public EMCC() {
    }

    public EMCC(String message, String phone) {
        this.message = message;
        this.phone = phone;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getDistrict() {
        return district;
    }

    public void setDistrict(String district) {
        this.district = district;
    }

    public String getMessage() {
        return message;
    }

    public void setMessage(String message) {
        this.message = message;
    }

    public String getPhone() {
        return phone;
    }

    public void setPhone(String phone) {
        this.phone = phone;
    }

    public String getMeterReading() {
        return meterReading;
    }

    public void setMeterReading(String meterReading) {
        this.meterReading = meterReading;
    }

    public String getMeterNumber() {
        return meterNumber;
    }

    public void setMeterNumber(String meterNumber) {
        this.meterNumber = meterNumber;
    }

    public String getAccountNumber() {
        return accountNumber;
    }

    public void setAccountNumber(String accountNumber) {
        this.accountNumber = accountNumber;
    }

    @Override
    public String toString() {
        return "EMCC{" + "message=" + message + ", phone=" + phone + ", name=" + name + ", meterReading=" + meterReading + ", meterNumber=" + meterNumber + ", district=" + district + ", accountNumber=" + accountNumber + '}';
    }

}
